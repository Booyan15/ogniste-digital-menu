function menuItem(
  id,
  categoryId,
  price,
  nameMk,
  nameEn,
  descriptionMk = '',
  descriptionEn = '',
  options = {},
) {
  const fallback = defaultDescriptions[categoryId] || { mk: '', en: '' };
  const displayPrice = options.displayPrice || String(price);

  return {
    id,
    categoryId,
    name: { mk: nameMk, en: nameEn },
    description: {
      mk: descriptionMk || fallback.mk,
      en: descriptionEn || fallback.en,
    },
    price,
    priceLabel: {
      mk: `${displayPrice} ден.`,
      en: `${displayPrice} MKD`,
    },
    image: productImages[id] || null,
    tags: options.tags || [],
  };
}

